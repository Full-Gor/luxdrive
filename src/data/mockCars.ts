// Initialiser les voitures dans le localStorage
export const initializeCars = () => {
  const existingCars = localStorage.getItem('luxdrive_cars');
  if (!existingCars) {
    localStorage.setItem('luxdrive_cars', JSON.stringify(mockCars));
  }
};

// Récupérer les voitures depuis le localStorage
export const getCars = (): Car[] => {
  const cars = localStorage.getItem('luxdrive_cars');
  return cars ? JSON.parse(cars) : mockCars;
};

